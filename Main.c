//Main Driving Loop
task driveLoop(){
	while(1 == 1)
	  {
	    motor[rightMotor] = vexRT[Ch2];
	    motor[leftMotor] = vexRT[Ch3];
	    EndTimeSlice();
	  }
}

//Drive Fast
task fastDriveLoop(){
	while(1==1){
		short rightMotorTurn = 0;
		short leftMotorTurn = 0;
		if(vexRT[Ch1] < 0){
			leftMotorTurn = vexRT[Ch1] * -1;
		} else{
			rightMotorTurn = vexRT[Ch1];
		}
		motor[rightMotor] = 255 - rightMotorTurn;
		motor[leftMotor] = 255 - leftMotorTurn;
		EndTimeSlice();
	}
}

task main()
{
  startTask(driveLoop);
  while(1==1){

  //Accelerate
	 	if(vexRT[Btn6D] == 1){
	 		stopTask(driveLoop);
	 		startTask(fastDriveLoop);
	 	}
	 	else{
	 		stopTask(fastDriveLoop);
	 		startTask(driveLoop);
	 	}


	 	//Intake Motors
 	 if(vexRT[Btn5U] == 1){
 		motor[intakeLeft] = 255;
 		motor[intakeRight] = 255;
	 	}
	 	else{
	 		motor[intakeLeft] = 0;
	 		motor[intakeRight] = 0;
	 	}


	 	//Outtake Motors
	 	if(vexRT[Btn5D] == 1){
 		motor[outtakeLeft] = 255;
 		motor[outtakeRight] = 255;
	 	}
	 	else{
	 		motor[outtakeLeft] = 0;
	 		motor[outtakeRight] = 0;
	 	}


	 	EndTimeSlice();
	}

}




